const freeMints: Record<string, number> = {
  hx1111111111111111111111111111111111111111: 1,
  hx2222222222222222222222222222222222222222: 2,
  hx3333333333333333333333333333333333333333: 3,
  hx4444444444444444444444444444444444444444: 4,
  hx5555555555555555555555555555555555555555: 5,
  hx6666666666666666666666666666666666666666: 6,
  hx7777777777777777777777777777777777777777: 7,
  hx8888888888888888888888888888888888888888: 8,
  hx9999999999999999999999999999999999999999: 9,
}

export default defineEventHandler((event): number => {
  const query = useQuery(event)

  if (query.address && typeof query.address === 'string') {
    return freeMints[query.address] || 0
  }

  return 0
})
